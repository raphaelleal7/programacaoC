#include <stdio.h>

int main() {

    /*
    Incremento (++)
    Pré-Incremento ++a
    Pós-Incremento a++
    Decremento (--)
    Pré-Decremento --a
    Pós-Decremento a--
    */

    int numero1 = 1, resultado;

    printf("Antes Incremento: %d\n", numero1);

    resultado = numero1++;
    printf("Após Pós-incremento - Numero 1: %d - Resultado: %d\n", numero1, resultado);




return 0;


}